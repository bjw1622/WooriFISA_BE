// 하나의 java파일은 하나의 클래스를 가지고 있음
// Hello.java 파일은 하나의 class인 Hello를 가지고 있음
public class Hello{
    // Hello class가 가진 main() 메서드
    public static void main(String[] args){
        System.out.println("Hello Java");
    } // end main()
} // end Hello
